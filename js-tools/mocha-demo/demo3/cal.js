/**
 *  add two number x and y
 *  @param {number} x The first number to add
 *  @param {mumber} y The second number to add
 *  @return {number} The result x + y
 */
function add(x, y) {
  return x + y;
}

/**
 *  multiply two number x and y
 *  @param {number} x The first number to multiply
 *  @param {mumber} y The second number to multiply
 *  @return {number} The result x * y
 */
function multiply(x, y) {
  return x * y;
}

module.exports.add = add;
module.exports.multiply = multiply;
